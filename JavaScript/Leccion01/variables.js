// LET => Definir variables de la forma recomendada

let color;
color = "Rojo";
console.log(color);

// Varias variables en una linea
let x,y,z;
x = 10;
y = 20;
z = x+y;
console.log(z);

// CONST => Definir variable que no se requiera modificar su valor
const pi = 3.14;
console.log(pi);
// pi = "3.14" => arroja error por lo que const no permite modificar el valor

